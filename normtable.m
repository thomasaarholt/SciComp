%% normtable()
% Produces a norm for HW3 in Scientific Computing at Oxford

function [] = normtable(xx,N) 
fprintf('================================================================================\n'); 
fprintf('            Backslash        Normal equation          QR                  SVD        \n') 
fprintf('                                                                          \n')
fprintf('================================================================================\n'); 
    fprintf('x');
    fprintf('        %+1.15f  %+1.15f  %+1.15f  %+1.15f\n',xx);
fprintf('--------------------------------------------------------------------------------\n');    
    fprintf('2-norm  %+1.15f  %+1.15f  %+1.15f  %+1.15f\n',N);
fprintf('================================================================================\n');
end
